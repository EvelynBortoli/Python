#Condicionales elif


dia= input("Que dia es? ")
dia = int(dia)

if dia == 1:
    print("Hoy es Lunes")

elif dia == 2:
    print("Hoy es Martes")

elif dia == 3:
    print("Hoy es Miércoles")

elif dia == 4:
    print("Hoy es Jueves")

elif dia == 5:
    print("Hoy es Viernes")

elif dia == 6:
    print("Hoy es Sábado")

elif dia == 7:
    print("Hoy es Domingo")
    print("Día de descanso!")

else: print("Día inválido!!!")